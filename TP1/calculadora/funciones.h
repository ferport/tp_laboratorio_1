#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED



#endif // FUNCIONES_H_INCLUDED

/** \brief calcula la suma entre dos numeros enteros
 *
 * \param primer numero para sumar
 * \param segundo numero para sumar
 * \return la suma entre los numeros
 *
 */

int sumar(int num1,int num2);

/** \brief calcula la resta entre dos numeros enteros
 *
 * \param primer numero para restar
 * \param segundo numero para restar
 * \return la resta entre los numeros
 *
 */

int restar(int num1, int num2);

/** \brief calcula la division entre dos numeros
 *
 * \param primer numero para dividir
 * \param segundo numero para dividir
 * \return la division entre los numeros
 *
 */

float dividir(float num1,float num2);

/** \brief calcula la multiplicacion entre dos enteros
 *
 * \param primer numero para multiplicar
 * \param segundo numero para multiplicar
 * \return la multiplicacion entre los numeros
 *
 */

int multiplicar(int num1, int num2);

/** \brief calcula el factorial de un numero entero
 *
 * \param numero al cual sacar el factorial
 * \return el factorial del numero. Si retorna -1 indica que hay un desbordamiento
 *
 */

int factorial(int num);
